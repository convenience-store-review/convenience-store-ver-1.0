package org.cvs.domain;

public class Grade {

	private int id;
	private double taste;
	private double cost_ratio;
	private double calory;
	private double total;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getTaste() {
		return taste;
	}
	public void setTaste(double taste) {
		this.taste = taste;
	}
	public double getCost_ratio() {
		return cost_ratio;
	}
	public void setCost_ratio(double cost_ratio) {
		this.cost_ratio = cost_ratio;
	}
	public double getCalory() {
		return calory;
	}
	public void setCalory(double calory) {
		this.calory = calory;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	
	
	
}
